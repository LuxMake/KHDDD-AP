--Level up prompt script
local PromptTask = {}



function PromptTask:WritePromptToMem()
	local freeMem = 0xA983F8
	local taskArr = {0x51, -- push rcx
	 0x52, --push rdx
	 0x41, 0x50, --push r8
	  0x41, 0x51, --push r9
	  0x48, 0xB9, 0x01,
	  0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, --mov rcx
	  0x48, 0xBA, 0x03,
	  0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, --mov rdx
	  0x49, 0xB8, 0x05,
	  0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, --mov r8
	  0x49, 0xB9, 0x01,
	  0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, --mov r9
	  0xE8, 0x3D, 0x2F, 0x1A, 0x00, --call "khddd.exe"+192F70
	  0x41, 0x59, --pop r9
	  0x41, 0x58, --pop r8
	  0x5A, --pop rdx
	  0x59, --pop rcx
	  0xC3 --ret (maybe exclude)
	}

	WriteArray(freeMem, taskArr)
	ConsolePrint("Wrote function to memory")
end

function PromptTask:CallPrompt()
	--local callInstructions = {0xE8, 0xF4, 0x83, 0xA9, 0x00}
	local callInstructions = {0xE9, 0xF4, 0x83, 0xA9, 0x00} 
	local freeMem = 0xA983F2
	
	WriteArray(freeMem, callInstructions)

	ConsolePrint("Attempting to call the function")
end

return PromptTask